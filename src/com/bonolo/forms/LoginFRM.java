    public void displayFrames(final Component comp)
    {
        comp.addMouseListener(new MouseAdapter() 
        {
            public void mouseClicked(MouseEvent e)
            {
                int count = e.getClickCount();
                if(comp.equals(lblHome) || comp.equals(txtHome))
                {
                    if(count ==1)
                    {
                        setVisible(false);
                        new MainFRM().setVisible(true);
                    }
                    
                }else if(comp.equals(lblForgotPassword))
                {
                    if(count ==1)
                    {
                        JOptionPane.showMessageDialog(rootPane, "You're getting old");
                    }
                    
                }
            
            }
        
        });
    
    
    }
    

