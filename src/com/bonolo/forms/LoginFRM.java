    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
       if(((String)comboUser.getSelectedItem()).equals("Client"))
       {
           ClientDAO cd = new ClientDAO();
           try
           {
               
               cd.openConnect();
               
               if(cd.login(new Login(this.txtUsername.getText() , String.valueOf(this.txtPassword.getPassword() ), (String)this.comboUser.getSelectedItem())))
               {
                   
                   
               }
               
           }catch(Exception ex)
           {
               JOptionPane.showMessageDialog(rootPane, "Error " + ex.getMessage());
           
           }finally
           {
           
           
           }
       
       }else if(((String)comboUser.getSelectedItem()).equals("Administrator"))
       {
           AdminDAO dao = new AdminDAO();
           try
           {
               dao.openConnect();
               if(dao.login(new Login(this.txtUsername.getText(),String.valueOf(this.txtPassword.getPassword()),(String)this.comboUser.getSelectedItem() )) > 0)
               {
                   setVisible(false);
                   new AdminFRM().setVisible(true);
               }
           }catch(Exception ex)
           {
               JOptionPane.showMessageDialog(rootPane, "Error " + ex.getMessage());
           }finally
           {
               try {
                   dao.closeConnect();
               } catch (SQLException ex) {
                   Logger.getLogger(LoginFRM.class.getName()).log(Level.SEVERE, null, ex);
               }
           }
       
       
       }
        
        
    }//GEN-LAST:event_btnLoginActionPerformed

